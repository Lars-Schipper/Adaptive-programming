package oefenopdracht;

public class student {

    String naam;
    int leeftijd;
    int cijferlijst;
    String huisdier;

    public student(String naam, int leeftijd, int cijferlijst, String huisdier){
        this.naam = naam;
        this.leeftijd = leeftijd;
        this.cijferlijst = cijferlijst;
        this.huisdier = huisdier;
    }
}
